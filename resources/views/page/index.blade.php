                    <h2 class="title"><span>ふりスタとは？</span></h2>
                </header>
                <div class="post">
                    <p>「 Free　Style　Study　System 」（ フリー・スタイル・スタディ・システム ）の略称で、「  学習時間や学習指導教科において、『できる限り自由であること』 」を目的としています。個別指導も集団指導も選択出来る、「 理想的な学習塾の最終形態 」です。</p>
                    <h3>☆『 Free　Style 』（学習指導形態）</h3>
                    <p><img src="images/banners/desc1.png" alt="" width="201" class="alignleft">
                        <span class="strong">A. 「 家庭教師・個別指導プラン 」〔 1対1・担任制完全個別スタイル 〕</span><br>
                        ・①次のテストまでの日数、②生徒が学習指導を希望する教科(単元)、➂教室長の指定する学習すべき教科(単元)を考慮し、5～7種類の学習の選択肢を毎回用意します。指導開始前に生徒と相談して、その日の学習内容を決定します。一方的にその日の指導内容を決めることはありません。<br>
                        ・当塾では決まった教材がありません。生徒が持ち込んだ教材でも、学校のワーク類でも、他塾のテキストでも指導します。統一教材を機械的に進める他塾とは、指導方針が根本的に異なります。<br>
			・対策が早くて具体的なので、概ね次のテストで結果が出ることが多いです。「 結果が出たので、学習時間が増える、やる気に繋がる 」という好循環を生みます。自主的に学習するようになるので、成績が上がり続けます。<br>・自分だけのペースで学習したい生徒さんは、こちらがお勧めです。<br>
<br>
                        <span class="strong">B. 「１対2ペア指導プラン」</span><br>
			・学年が異なり、教科が異なる場合、学習効果を上げるギリギリの人数になります。１対2指導まで、教科を自由にお選びいただけます。他の生徒と学習するのが気にならない生徒さんは、こちらがお勧めです。<br><br>

                        <span class="strong">C. 「 フリータイムスタンダードプラン 」〔 小・中学生対象：少人数個別指導 〕</span><br>
			※　１クラス３人以下の少人数個別指導クラスです。<br>
                        ・90分～120分の問題演習に特化したコースになります。かなり厳しい内容なので、120分が限界になります。<br>
・毎年3月～6月までは不定期ですが、7月以降は定期的に開講します。<br>
また季節講習期間中は、ほぼ毎日学習指導を行います。<br>
・およそ４００枚以上のバラプリント（塾オリジナル）を使い、演習を繰り返します。弱点克服のスピードが他塾とは圧倒的に異なります。<br>
・学習内容と指導日時は教室の指定になります。とにかく、１問でも多く問題を解きたい生徒さんは、こちらがお勧めです。<br>
<br>

                        <span class="strong">D．「 ゲスト模擬試験受験生プラン 」〔 模擬試験の受験のみを希望する場合 〕</span><br>
                        ・『 ふりスタ 』では『 新みやぎ模試 』が受験可能です（ 受験後の成績表は郵送になります ）。<br>
・模擬試験受験後も、自宅訪問や電話をかけるなどのしつこい勧誘は一切しません。<br>・毎年5〜6人ほど、模擬試験だけの受験希望者がいます。<br>
<br>
<span class="strong">☆とことん・ふりスタ・システム</span><br>
1対1指導の場合、月謝が高額になりがちなので、月謝の上昇を抑えるシステムです。1対1指導で弱点を克服し、スタンダード指導で問題を解きまくる理想的な学習スタイルです。また、成績不振者を対象に「補講指導」も行います。生徒の理解度・学習速度にとことん合わせるシステムです。
<br>
<br>
<span class="strong">☆ 『 成績保証制度 』</span><br>
【 対象：①入塾時、中学２年生以下であること ②1対1指導であること】<br>
A．【 対象：(校外)模擬試験 】<br>
①入塾時より偏差値を上げることを目標にします。<br>
