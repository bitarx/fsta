                    <h3>５.『 振り返り　Study 』（ 選択可能な指導内容 ）</h3>
                    <p><img src="images/banners/mendan_boy.png" alt="" width="231" class="alignleft">B.「 エクストラ・個別指導プラン 」の場合、模試対策、定期試験対策、検定試験対策、歴史や化学反応式の学習し直しなど、教室長が指導方針を決め、学習担当講師と生徒が相談してその日の学習内容を決めます。一方的にその日の指導内容を決めることはありません。<br><br><br><br><br></p>
                    <h3>６.『 Free　Selection 』（ 自由な問題選択 ）</h3>
                    <p><img src="images/banners/classroom.png" alt="" width="231" class="alignright">A.「 スタンダード・少人数プラン 」の場合、その日のテーマを講師とともに確認します。<br>
その後、BASIC基礎確認、反復TRAINING、実戦CHALLENGE、から問題を選択します。<br>
・BASIC基礎確認……　学校の授業内容レベルの問題となります。BASIC（ ベーシック、基礎 ）の確認を行います。<br>
・反復TRAINING………　50点の得点または偏差値50突破を目指します。TRAINING（ トレーニング ）を積んで、とにかく沢山の問題を解いて頂きます。<br>
・実践CHALLENGE…　80点以上の突破を目指します。難問・奇問、何でも挑戦します。<br><br>

※   B.「 エクストラ・個別指導プラン」においても、上記の問題選択は可能です。担当講師もしくは、教室長にご相談ください。
<br><br></p>
                    <h3>７. 『 Free　Star 』を一緒に目指しましょう</h3>
                    <p><img src="images/banners/success.png" alt="" width="231" class="alignleft">生徒ひとり一人の性格や適性は異なるものです。一斉集団指導の場においてすら、その学習指導方法にこだわるのが、本当の意味での「 個別指導 」であります。<br>
他の人よりも頑張って、他の人よりも問題を1問でも多く解いて、「 立派な自由な星 」のように輝きましょう。</p>
